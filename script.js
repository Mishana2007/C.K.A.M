const carouselInner = document.querySelector('.carousel-inner');
const testimonialCards = document.querySelectorAll('.testimonial-card');
const prevButton = document.querySelector('.prev-button');
const nextButton = document.querySelector('.next-button');
const dots = document.querySelectorAll('.dot');

let currentPosition = 0;

nextButton.addEventListener('click', () => {
  currentPosition = (currentPosition + 1) % testimonialCards.length;
  updateCarousel();
});

prevButton.addEventListener('click', () => {
  currentPosition = (currentPosition - 1 + testimonialCards.length) % testimonialCards.length;
  updateCarousel();
});

function updateCarousel() {
  carouselInner.style.transform = `translateX(-${currentPosition * 100}%)`;

  // –û–±–Ω–æ–≤–ª—è–µ–º –∞–∫—Ç–∏–≤–Ω—É—é —Ç–æ—á–∫—É
  dots.forEach((dot, index) => {
    if (index === currentPosition) {
      dot.classList.add('active');
    } else {
      dot.classList.remove('active');
    }
  });
}

// –î–æ–±–∞–≤–ª—è–µ–º –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —Å–≤–∞–π–ø–æ–º
let touchStartX = 0;

carouselInner.addEventListener('touchstart', (e) => {
  touchStartX = e.touches[0].clientX;
});

carouselInner.addEventListener('touchmove', (e) => {
  if (!touchStartX) return;

  const touchCurrentX = e.touches[0].clientX;
  const diffX = touchStartX - touchCurrentX;

  if (Math.abs(diffX) > 50) { // Adjust the threshold as needed
    if (diffX > 0) {
      nextButton.click();
    } else {
      prevButton.click();
    }
    touchStartX = 0;
  }
});

// –î–æ–±–∞–≤–ª—è–µ–º –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è –ø–æ —Ç–æ—á–∫–∞–º
dots.forEach((dot, index) => {
  dot.addEventListener('click', () => {
    currentPosition = index;
    updateCarousel();
  });
});
